@dataclasses.dataclass
class WayPoint:
    point_x: tp.Optional[int]
    point_y: tp.Optional[int]

    @classmethod
    def from_request(cls, point: tp.Tuple[int, int]) -> WayPoint:
        return cls(point_x=point[0], point_y=point[1])


@dataclasses.dataclass
class DataPoints:
    start: WayPoint
    target: WayPoint

    @classmethod
    def from_request(cls, json):
        point_start_x = int(json['point_start_x'])
        point_start_y = int(json['point_start_y'])
        point_target_x = int(json['point_target_x'])
        point_target_y = int(json['point_target_y'])

        return cls(start=WayPoint.from_request((point_start_x, point_start_y)),
                   target=WayPoint.from_request((point_target_x, point_target_y)))


class Graph:
    def __init__(self):
        self.edges: dict[Location, list[Location]] = {}

    def neighbors(self, id: Location) -> list[Location]:
        return self.edges[id]


class WeightedGraph(Graph):
    def cost(self, from_id: Location, to_id: Location) -> float:
        return 1


class SquareGrid:
    def __init__(self, width: int, height: int, maze):
        self.width = width
        self.height = height
        self.walls: list[GridLocation] = []

        for i in range(width):
            for j in range(height):
                if maze[i][j] == 1:
                    self.walls.append((i, j))

    def in_bounds(self, id: GridLocation) -> bool:
        (x, y) = id
        return 0 <= x < self.width and 0 <= y < self.height

    def passable(self, id: GridLocation) -> bool:
        return id not in self.walls

    def neighbors(self, id: GridLocation) -> tp.Iterator[GridLocation]:
        (x, y) = id
        neighbors = [(x + 1, y), (x - 1, y), (x, y - 1), (x, y + 1),
                     (x + 1, y + 1), (x - 1, y - 1), (x - 1, y + 1),
                     (x + 1, y - 1)]  # E W N S:

        results = filter(self.in_bounds, neighbors)
        results = filter(self.passable, results)
        return results


class GridWithWeights(SquareGrid):
    def __init__(self, width: int, height: int, maze: np.array):
        super().__init__(width, height, maze)
        self.weights: dict[GridLocation, float] = {}

    def cost(self, from_node: GridLocation, to_node: GridLocation) -> float:
        return self.weights.get(to_node, 1)
